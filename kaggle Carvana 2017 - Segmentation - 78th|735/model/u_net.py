    x = create_tiramisu(num_classes, inputs, nb_layers_per_block=[4,5,7,10,12,15], p=0.45, wd=1e-3)
    model = Model(inputs, x)
    model.compile(optimizer=RMSprop(1e-6, decay=7e-7), loss=bce_dice_loss, metrics=[dice_metric])
